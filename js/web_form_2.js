document.getElementById("right-side-add-button").addEventListener("click", function() {
    let productName = document.getElementById("pName").value;
    let name = document.getElementById("name").value;
    let unitPrice = document.getElementById("uPrice").value;
    let quantityInStock = document.getElementById("quaStock").value;
    let supplier = document.getElementById("suppliers").value;
    let date = document.getElementById("date").value;

    alert("Product Name : " + productName + "\n Name : " + name + "\n Unit Price : " + unitPrice + "\n Quantity In Stock : " + quantityInStock + "\n Supplier : " + supplier + "\n Date : " + date);
});

